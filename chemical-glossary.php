              <h3>MUD CHEMICALS</h3><br>
              <p>
                Drilling muds or drilling fluids are a thick, viscous fluid mixture that is used in drilling operations to carry rock cuttings to the surface, and also lubricate as well as cool the drill bit.

              </p><br>
              <p>
                Mud Chemicals are water/oil-or-synthetic based mud used in the water well drilling operation.<br>

                The main functions of drilling fluids include <br>
              </p>
              <li>Providing hydrostatic pressure to prevent formation fluids from entering into the wellbore i.e. preventing the collapse of unstable strata into the borehole.</li><br>
              <li>Keeping the drill bit cool, lubricated and clean.</li><br>
              <li>Carrying out drill cutting operations.</li><br>
              <li>Ensure adequate formation evaluation</li><br>
              <li>Suspending the drill cuttings while drilling is paused and when the drilling assembly is brought in and out of the borehole.</li><br>
              <li>Facilitate cementing and completion</li><br>
              <li>Avoid formation damage and limit corrosion.</li><br>

              <p></p><br>
              <p>
                Drilling muds are divided into<br>
                Water-based drilling muds<br>
                Freshwater, seawater, naturally occurring brines or prepared brines<br>
                Usually done at conventional vertical wells at medium depths.<br>
                Oil-based drilling muds<br>
                Direct products of petroleum refining i.e mineral oil or diesel oil.<br>
                Better for greater depths or directional/horizontal drilling.<br>
                Requires greater stress on the drilling equipment.<br>
                Synthetic based drilling muds<br>
                Highly refined fluid compound <br>

              </p><br>
              <p>
                They provide primary well control of sub-surface pressures by a combination of density and any additional pressure acting on the fluid column (both annular and surface imposed).<br>
                Some Mud Chemicals are Barites, Calcium Carbonate, Calcium Chloride, Drilling Detergent, Glutaraldehyde, Amine based oxides, EP Lubricants, H2S Scavenger, Oxalic Acid, Soda Ash, Bentonite, Gaur Gum etc.<br>
                E.g. Baritie(Barium Sulfate) to increase the weight of column to stabilize the borehole, d OR Bentonite(to give enough viscosity to carry out cutting chips to the surface.<br>
                Many types of drilling fluids are used on a day-to-day basis. Along with their formations, these are used along with appropriate polymer and clay additives for drilling various oil and gas formations.<br>
                A mud engineer is an individual who is charged to maintain the drilling fluid or completion fluid system on an oil/gas drilling rig.<br>
                Drilling muds are classified on the basis of the fluid phase, alkalinity, dispersion and the type of chemicals used.<br>

              </p><br>
              <p>
                Some wells require different types that are to be used at different parts in the hole, or for some types require a combination with others.

              </p><br>
              <p>
                The various types of fluidare:<br>
                • Air: Compressed air is pumped either down the bore hole's annular space or down the drill string itself.<br>
                • Air/water: same as above, with water added to increase the viscosity<br>
                • Air/polymer: A specially formulated polymer, is added to the water and air mixture to create specific conditions.<br>
                • Water: In offshore drilling, seawater is typically used while drilling the top section of the hole.<br>

              </p><br>
              <p>
                • Water-based mud (WBM): Most basic water-based mud systems begin with water, then clays and other chemicals are incorporated into the water to create a homogeneous blend. The most common of these is bentonite, frequently referred to in the oilfield as "gel."

              </p><br>
              <p>
                • Oil-based mud (OBM): Oil-based mud is mud where the base fluid is a petroleum product such as diesel fuel. Oil-based muds are used for many reasons, including increased lubricity, enhanced shale inhibition, and greater cleaning abilities with less viscosity. Oil-based muds also withstand greater heat without breaking down.&nbsp;
              </p><br>
              <p>
                • synthetic-based fluid (SBM) (Otherwise known as Low Toxicity Oil Based Mud or LTOBM): synthetic-based fluid is mud in which the base fluid is a synthetic oil. This is most often used on offshore rigs because it has the properties of an oil-based mud, but the toxicity of the fluid fumes are much less than an oil-based fluid.&nbsp;

              </p><br>
              <h2>Cement Additives</h2>
              <p>
                Cement is made from calcareous and argillaceous rocks like clay, limestone and shale or any other material that contains a higher percentage of calcium carbonate.
                The dry material is grounded in fine powder and mixed with correct proportions and adjusted according to the need. This mix is known as kiln feed which is then heated to a certain temperature that results in a material called clinker<br>
                The clinker is cooled and mixed with gypsum (to control the setting and the hardening properties of cement slurry) and other products to form a new product called Portland cement.
                Additional chemicals are added to control fluid loss, rheology and slurry density to modify the behaviour of cement that allows cement placement under a wide range of downhole conditions.
                Thus, Cement additives are the materials that are added to cement to optimize cement properties along with the cement grinding process.
                <br>
                Cement additives can be classified into different types of product group such as grinding aids, to enhance strength and performance and affecting the production process by improving the quality of cement.
                These improvements can enhance cement properties depending on the application of the cement. e.g.
                <br>
              </p>
              <li>
                Powder flowability
              </li><br>
              <li>Strength development</li><br>
              <li>Mortar workability</li><br>
              <li>Durability</li>
              <p></p><br>
              <p>
                Cement additives are boric acid, hematite, tartaric acid, tannin acid, starch etc. in the form of accelerators, retarders, dispersants, extenders, gels, weighting agents, foamers and fluid loss additives.<br>
                1.Accelerators: A chemical additive that is used to speed up the normal rate of reaction between cement and water shortening the thickening time of cement and increasing its early strength.<br>
                2.Retarders: A chemical additives to decrease the speed of cement hydration and delay setting which allows sufficient time for slurry placement.<br>
                3. Extenders: Water absorbing materials which are used to reduce slurry density and increase the yield of cement slurry. This helps in preventing the breakdown of weak formation and loss of circulation<br>
                4.Weighting agents: To restrain high formation pressures, weighting agents are used, weighting agents are inert in the cement slurry<br>
                5.Dispersants: Friction reducing additives to improve the flow properties of cement slurry.<br>
                6.Fluid loss additives: Also known as permeability plugging additive to reduce the rate at which water from cement is forced into the permeable formation<br>
                7.Special agents: Materials like Resin or plastic cement, microfine cement, expanding cement, refractory cement, gypsum cement, cements for carbon dioxide resistance, cement additives for permafrost environments, and sorel cement, and latex cement comes under this category.<br>

              </p><br>
